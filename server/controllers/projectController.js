import{Request,Response} from 'express'
import prisma from "../lib/prisma.js";
import openai from "../configs/openai.js";
import { Prisma } from '../generated/prisma/client.js';


export const makeRevision=async(req:Request, res: Response)=>{
    const userId = req.userId;
    try{

        const{projectId} =req.params;
        const {message}= req.body;

        const user = await prisma.user.findUnique({
            where: {id:userId}
        })


       if(!userId || !user){
        return res.status(401).json({message:'Unauthorized'});
       }

       if(user.credits<2){
        return res.status(401).json({message:'add maore credits to make changes'});
       }

       if(!message || message.trim()===''){
        return res.status(400).json({message: 'Please enter a valid prompt'});
       }

       const currentProject =await prisma.websiteProject.findUnique({
        where:{id: projectId, userId},
        include: {version: true}
       })
       if(!currentProject){
        return res.status(400).json({message: 'Please enter a valid prompt'});
       }

       await prisma.conversation.create({
        data: {
            role:'user',
            content: message,projectId
        }
       })
       await prisma.user.update({
        where: {id: userId},
        data: {credits:{decrement: 5}
        }})

        //Enhance user prompt

        const promptEnhanceResponce = await openai.chat.completions.create({
            model: 'z-ai/glm-4.5-air:free',
            messages:[{
                 role:'system',
                 content: `You are a prompt enhancement specialist. The user wants to make changes to their website. Enhance their request to be more specific and actionable for a web developer.

    Enhance this by:
    1. Being specific about what elements to change
    2. Mentioning design details (colors, spacing, sizes)
    3. Clarifying the desired outcome
    4. Using clear technical terms

Return ONLY the enhanced request, nothing else. Keep it concise (1-2 sentences).`
            },{
                role:'user',
                content:`User's request: "${message}"`
            }]
        })

        const enhancedprompt = promptEnhanceResponce.choices.INDEX[0].message.content;
        await Prisma.Conversation.create({
            data:{
                role:'assistant',
                content:`I've enhanced your prompt to "${enhancedprompt}"`,
                projectId
            }
        })

        await Prisma.Conversation.create({
            data:{
                role:'assistant',
                content:`Now making changes to your website... "${enhancedprompt}"`,
                projectId
            }
        })

        //Genrate website code
        const codeGenrationResponse= await openai.chat.completions.create({
            model: 'z-ai/glm-4.5-air:free',
            messages:[{
                role: 'system',
                content: `You are an expert web developer. 

    CRITICAL REQUIREMENTS:
    - Return ONLY the complete updated HTML code with the requested changes.
    - Use Tailwind CSS for ALL styling (NO custom CSS).
    - Use Tailwind utility classes for all styling changes.
    - Include all JavaScript in <script> tags before closing </body>
    - Make sure it's a complete, standalone HTML document with Tailwind CSS
    - Return the HTML Code Only, nothing else

    Apply the requested changes while maintaining the Tailwind CSS styling approach.`
            },{
               role:'user',
               content:`Here is the current website code: "${currentProject.current_code}
               "The user wants this change:"${enhancedprompt}"` 
            }]
        })
      
       res.json({credits: user?.credits})
    }catch(error: any){
        console.log(error.code || error.message);
        res.status(500).json({message: error.message});


    }
}
